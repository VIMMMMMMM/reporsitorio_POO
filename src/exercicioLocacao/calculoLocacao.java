package exercicioLocacao;

public class calculoLocacao {
    private double valorDiaTotal;
    private  double valorHoraTotal;
    private  double valorTotal;
    private double valorImposto;
    private long duracaoLocacao;

    public long getDuracaoLocacao() {
        return duracaoLocacao;
    }

    public void setDuracaoLocacao(long duracaoLocacao) {
        this.duracaoLocacao = duracaoLocacao;
    }

    public double getValorDiaTotal() {
        return valorDiaTotal;
    }

    public void setValorDiaTotal(double valorDiaTotal) {
        this.valorDiaTotal = valorDiaTotal;
    }

    public double getValorHoraTotal() {
        return valorHoraTotal;
    }

    public void setValorHoraTotal(double valorHoraTotal) {
        this.valorHoraTotal = valorHoraTotal;
    }

    public double getValorTotal() {
        return valorTotal;
    }

    public void setValorTotal(double valorTotal) {
        this.valorTotal = valorTotal;
    }

    public double getValorImposto() {
        return valorImposto;
    }

    public void setValorImposto(double valorImposto) {
        this.valorImposto = valorImposto;
    }



}
